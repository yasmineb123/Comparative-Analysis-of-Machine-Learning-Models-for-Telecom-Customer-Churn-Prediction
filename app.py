# Dictionnaire pour stocker toutes les métriques
results = {}

def evaluate_model(name, model, X_tr, X_te, y_tr, y_te, use_proba=True):
    """Entraîne, évalue et retourne les métriques + prob pour ROC"""
    model.fit(X_tr, y_tr)
    y_pred = model.predict(X_te)
    if use_proba and hasattr(model, "predict_proba"):
        y_prob = model.predict_proba(X_te)[:, 1]
    elif hasattr(model, "decision_function"):
        y_prob = model.decision_function(X_te)
    else:
        y_prob = None

    acc  = accuracy_score(y_te, y_pred)
    prec = precision_score(y_te, y_pred, zero_division=0)
    rec  = recall_score(y_te, y_pred, zero_division=0)
    f1   = f1_score(y_te, y_pred, zero_division=0)
    cm   = confusion_matrix(y_te, y_pred)
    roc  = roc_auc_score(y_te, y_prob) if y_prob is not None else None

    results[name] = {
        "Accuracy": acc, "Precision": prec,
        "Recall": rec,   "F1": f1,
        "ROC-AUC": roc,  "y_prob": y_prob,
        "y_pred": y_pred, "CM": cm, "model": model
    }
    print(f"\n{'─'*40}")
    print(f"  {name}")
    print(f"  Accuracy : {acc:.4f}")
    print(f"  Precision: {prec:.4f}")
    print(f"  Recall   : {rec:.4f}")
    print(f"  F1-Score : {f1:.4f}")
    print(f"  ROC-AUC  : {roc:.4f}" if roc else "  ROC-AUC : N/A")
    print(f"\n  Rapport de classification:")
    print(classification_report(y_te, y_pred))
    return model

